/*
Arrow Funtions
*/


const sum =  (number1, number2) => {
    return number1 + number2
}
const number1 = 10
const number2 = 20

console.log (`O primeiro número é ${number1}`)
console.log (`o segundo número é ${number2}`)

const result = sum(number1, number2)

console.log (`E a soma dos dois é ${result}`)